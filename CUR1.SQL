SET SERVEROUTPUT ON;

DECLARE
 INAME SQCUBE%ROWTYPE;
 num number;
 CURSOR CUR1(MAXI INT) IS
  SELECT * FROM SQCUBE WHERE CUBE< MAXI;

BEGIN
 num:=&limit;
 
 OPEN CUR1(num);
  LOOP
   FETCH CUR1 INTO INAME;
   EXIT WHEN CUR1%NOTFOUND;
   DBMS_OUTPUT.PUT_LINE(INAME.NUM ||' --- ' || INAME.CUBE);
 END LOOP;
END;
/
  
