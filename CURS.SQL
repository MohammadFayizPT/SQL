 SET SERVEROUTPUT ON;

DECLARE
  
V_NUM NUMBER;
V_FORMAT VARCHAR(20);
V_CUBE NUMBER;
NOM NUMBER;

 CURSOR CURS(NUMS NUMBER) IS
  SELECT S.NUM,O.FORMAT,S.CUBE FROM SQCUBE S,ODDEVEN O WHERE S.NUM=O.NUM AND S.NUM=NUMS;
 

BEGIN
 NOM :=&INPUT;
 OPEN CURS(NOM);
 
 LOOP
  FETCH CURS INTO V_NUM,V_FORMAT,V_CUBE;
  IF CURS%NOTFOUND THEN
    DBMS_OUTPUT.PUT_LINE('SORRY');
END IF;
  EXIT WHEN CURS%NOTFOUND;
  DBMS_OUTPUT.PUT_LINE(V_NUM ||'----'|| V_FORMAT||'----'|| V_CUBE);
 END LOOP;
END;
/ 